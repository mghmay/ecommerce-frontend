<template>
  <div id="page-wrap">
    <ProductsGrid :products="products" />
  </div>
</template>
<script>
import axios from 'axios'
import ProductsGrid from '@/components/ProductsGrid.vue'

export default {
  name: 'ProductsPage',
  data() {
    return {
      products: []
    }
  },
  components: {
    ProductsGrid
  },
  async created() {
    const result = await axios.get('/api/products')
    const products = result.data
    console.log(result)
    this.products = products
  }
}
</script>
